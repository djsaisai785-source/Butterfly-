/** Retourne une image pour une catégorie donnée, de façon déterministe selon l'id de l'annonce */
export function getCategoryImage(
  category: string,
  id: string,
  universe: "butterfly" | "kido" | "aura" = "aura"
): string {
  const map =
    universe === "butterfly"
      ? BUTTERFLY_CATEGORY_IMAGES
      : universe === "kido"
      ? KIDO_CATEGORY_IMAGES
      : AURA_CATEGORY_IMAGES;

  const images = map[category] ?? map["default"] ?? [];
  if (images.length === 0) return "";

  // Index déterministe basé sur l'id pour éviter les changements aléatoires
  const idx = id
    ? id.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0) % images.length
    : 0;

  return images[idx]!;
}
